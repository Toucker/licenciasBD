    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String usuario = txtusu.getText();
        String password = jTextField1.getText();
        String nombre = txtnom.getText();
        String apellido = txtape.getText();
        String perfil="";
        if(jRadioButton1.isSelected()){
            perfil ="2";
        }else if(jRadioButton2.isSelected()){
            perfil="3";
        }
        String especialidad = Integer.toString(cboesp.getSelectedIndex()); 
        
        ArrayList<String> lista_errores = new ArrayList();
        if (usuario.trim().length() < 3) {
            lista_errores.add("Tiene que ingresar un usuario de 3 caracteres mínimo\n");
        }
        if (password.trim().length() < 8) {
            lista_errores.add("Tiene que ingresar una contraseña de 8 caracteres mínimo\n");
        }
        if (!(password.equalsIgnoreCase(jTextField2.getText()))) {
            lista_errores.add("Las contraseñas tienen que coincidir\n");
        }       
        if (nombre.trim().length() < 3) {
            lista_errores.add("Tiene que ingresar un nombre válido\n");
        }
        if (apellido.trim().length() < 3) {
            lista_errores.add("Tiene que ingresar un apellido válido\n");
        }
        if (especialidad.equalsIgnoreCase("0")) {
            lista_errores.add("Tiene que seleccionar una especialidad de médico\n");
        }
        
        if (lista_errores.isEmpty()) {

            if(jButton1.getText().equalsIgnoreCase("CREAR")){
                u = new Usuario(usuario,nombre,apellido,password,perfil,especialidad);
                d.registrarUsuario(u);
            }else{
                u = new Usuario(usuario,nombre,apellido,password,perfil,especialidad);
                d.modificarUsuario(u);
            }
            
            showM(false);
            cleanM();
            txtusu.setEnabled(true);
            jButton3.setVisible(false);
        }else{
            String errores = lista_errores.toString().replaceAll(",", "- ").replace("[","- ").replace("]","");
            javax.swing.JOptionPane.showMessageDialog(this,"Tiene los siguientes errores:"+"\n\n"+errores, "Error", 2);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

