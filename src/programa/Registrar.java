    private void txtusuKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtusuKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(jMenu1.isEnabled()){JOptionPane.showMessageDialog(this, "<html><b> ¡Error! </b><br>El servidor se encuentra desconectado.</html>");}
            else if(txtusu.getText().equalsIgnoreCase("MRivas")||txtusu.getText().equalsIgnoreCase("RFuentes")){JOptionPane.showMessageDialog(this, "<html><b> ¡Error! </b><br>No puede modificar está cuenta.</html>");}
            else if(d.consultarUsuario(txtusu.getText())==true){
                int dialogResult = JOptionPane.showConfirmDialog(this, "<html> <b> ¡El usuario ya existe! </b><br>¿Desea modificarlo?</html>", "", JOptionPane.YES_NO_OPTION);
                if (dialogResult == 0){
                    jButton1.setText("ACTUALIZAR");
                    u = d.cargarUsuario(txtusu.getText());
                    showM(true);
                    jButton3.setVisible(true);
                    txtusu.setEnabled(false);
                    txtnom.setText(u.getNombre());
                    txtape.setText(u.getApellido());
                    jTextField1.setText(u.getPassword());
                    jTextField2.setText(u.getPassword());                    
                    if(u.getPerfil().equals("2")){
                        jRadioButton1.setSelected(true);
                        jRadioButton1.setSelected(false);
                    }else{
                        jRadioButton2.setSelected(true);
                        jRadioButton2.setSelected(false);
                    }
                    cboesp.setSelectedIndex(Integer.parseInt(u.getEspecialidad()));
                }
            }else{
                jButton1.setText("CREAR");
                showM(true);
                txtusu.setEnabled(false);
            }
        }
    }//GEN-LAST:event_txtusuKeyPressed

